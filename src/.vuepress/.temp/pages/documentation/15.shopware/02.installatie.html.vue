kan je de verzendmethode actieveren en opslaan. In de verkoop kanalen kan je
PostNL activeren door op de dropdown onder verzending te klikken. Selecteer &quot;
PostNL&quot; in het betalingen en verzendmethodes scherm. Hier kan je ook PostNL als
standaard verzendmethode instellen. Als je klaar bent met het instellen, kan je
de cache
herladen: <code>Instellingen -&gt; Systeem -&gt; Caches &amp; Indexes -&gt; Verwijder en actieveer</code></p>
