<h2 id="verwerkingstijd" tabindex="-1"><a class="header-anchor" href="#verwerkingstijd" aria-hidden="true">#</a> Verwerkingstijd</h2>
<p>Je kunt hier instellen hoeveel dagen jij nodig hebt om een order te verwerken.
Als je bijvoorbeeld &quot;1&quot; invult, zal de check-out niet de volgende werkdag tonen,
maar een dag later. Bij geen verwerkingstijd vul je hier een &quot;0&quot; (nul)
in.</p>
