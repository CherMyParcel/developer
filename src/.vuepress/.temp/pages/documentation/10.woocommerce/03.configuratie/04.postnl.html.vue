<h2 id="sluitingstijd" tabindex="-1"><a class="header-anchor" href="#sluitingstijd" aria-hidden="true">#</a> Sluitingstijd</h2>
<p>Met de sluitingstijd kun je aangeven tot welk moment van de dag de orders nog
verwerkt kunnen worden. Als je elke dag rond 17:00 de bestellingen naar een
PostNL-locatie brengt is het handig om de laatste order rond 16:00 te verwerken.
Wanneer jouw klanten na dit tijdstip een bestelling plaatsen zien zij een latere
levermogelijkheid.</p>
<p>Bijvoorbeeld: Sluitingstijd is om 16:00 en je kunt dezelfde dag de orders
verwerken en op tijd inleveren. Als er na 16:00 besteld wordt, zal de checkout
niet een volgende dag levering laten zien.</p>
