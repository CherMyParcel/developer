<h2 id="standaard-verzendopties" tabindex="-1"><a class="header-anchor" href="#standaard-verzendopties" aria-hidden="true">#</a> Standaard verzendopties</h2>
<p>Op basis van de ingevulde orderwaarde worden de onderstaande opties geactiveerd.
Voorbeeld: Indien je bij <code>Verzekerd tot € 250</code> bijvoorbeeld de waarde 100
invult, worden orders met een verkoopwaarde boven de € 100 automatisch verzekerd
tot een waarde van € 250. Deze logica is voor alle onderstaande opties in te
stellen:</p>
<ul>
<li>Alleen huisadres</li>
<li>Handtekening bij ontvangst</li>
<li>Retour bij niet aanwezig</li>
<li>Groot pakket</li>
<li>Verzekerd tot € 250</li>
<li>Verzekerd tot € 500</li>
</ul>
<Message title="" type="note"><p>Let op: Op het moment dat <code>Nee</code> ingevuld staat, is de optie niet geactiveerd.
Bij invullen van <code>Ja</code> staat de optie actief. Wanneer er bij 'Vanaf prijs' een 0
wordt ingevuld zal de optie ook actief zijn.</p>
</Message><h2 id="leeftijdscheck-18" tabindex="-1"><a class="header-anchor" href="#leeftijdscheck-18" aria-hidden="true">#</a> Leeftijdscheck 18+</h2>
<p>De leeftijdscheck is bedoeld voor pakketzendingen waarvoor de ontvanger 18+ moet
zijn en dit moet bewijzen door middel van een identiteitsbewijs. Met deze
verzendoptie zijn handtekening voor ontvangst en alleen huisadres inbegrepen. De
leeftijdscheck is verder uitgesloten van de bezorgopties in de ochtend en
avondlevering.</p>
