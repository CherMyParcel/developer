en <code>klantengroepen</code> alle opties.</li>
<li>Klik links boven op <code>Voorwaarden</code> en schrijf zelf de regel door op <code>+</code> te
klikken. (voorbeeld: Subtotaal gelijk aan of groter dan 50)</li>
<li>Ga naar de volgende stap <code>acties</code> en geef bij <code>gratis verzending</code>
aan <code>Alleen voor betreffende artikelen</code>.</li>
<li>Klik rechts bovenin op opslaan.</li>
