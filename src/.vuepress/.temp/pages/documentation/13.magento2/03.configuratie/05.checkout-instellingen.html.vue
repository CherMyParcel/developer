<h2 id="extra-vroeg-ophalen-bij-postnl-locatie" tabindex="-1"><a class="header-anchor" href="#extra-vroeg-ophalen-bij-postnl-locatie" aria-hidden="true">#</a> Extra vroeg ophalen bij PostNL-locatie</h2>
<p>Ook voor het ophalen op een PostNL-locatie is er een mogelijkheid om dit
specifiek in de ochtend te doen (mogelijk vanaf 09:00 in plaats vanaf 16:00).
