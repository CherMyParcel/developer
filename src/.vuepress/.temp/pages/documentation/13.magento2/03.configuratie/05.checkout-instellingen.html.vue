<h3 id="laatste-bestelmoment" tabindex="-1"><a class="header-anchor" href="#laatste-bestelmoment" aria-hidden="true">#</a> Laatste bestelmoment</h3>
<p>Hiermee kun je instellen tot welk moment het mogelijk is voor jou een bestelling
klaar te maken voor levering. Indien jij hier tot 16:00 uur instelt, zullen alle
klanten na dit tijdstip een latere dag in hun checkout te zien krijgen als
eerste levermogelijkheid. Bijvoorbeeld: Jij hebt een verwerktijd van 1 dag en
kunt tot 16:00 bestellingen verwerken. De bestelling komt binnen op dinsdag 04
oktober om 16:02, de eerst volgende mogelijkheid voor jouw klant zou dan
donderdag 06 oktober worden. Dit betekent voor jou dat je het pakket op woensdag
zal inleveren bij een PostNL punt.</p>
<MPImg src="/documentation/magento/magento-laatste-bestelmoment.jpg" alt="Magento laatste bestelmoment" />
<h3 id="verzenddagen" tabindex="-1"><a class="header-anchor" href="#verzenddagen" aria-hidden="true">#</a> Verzenddagen</h3>
<p>Bij de verzenddagen kun je invullen op welke dagen het voor jou mogelijk is
pakketten in te leveren voor collectie op dezelfde dag. In de checkout wordt
hier rekening mee gehouden qua keuzes die worden getoond aan jouw klant.</p>
<MPImg src="/documentation/magento/magento-verzenddagen.jpg" alt="Magento verzenddagen" />
<h3 id="maandagbezorging" tabindex="-1"><a class="header-anchor" href="#maandagbezorging" aria-hidden="true">#</a> Maandagbezorging</h3>
<p>Er kan ook gekozen worden om een pakket op maandag te laten bezorgen, houd er
rekening mee dat je bij verzenddagen wel de zaterdag selecteert als verzenddag.</p>
<Message title="" type="note"><p>Let op: Maandaglevering is alleen mogelijk wanneer het laatste
bestelmoment voor maandagbezorging op 14:30 is ingesteld en zaterdag het pakket
voor 15:00 wordt geleverd bij één van de hiervoor bestemde <AutoLink item="https://www.myparcel.nl/blog/">PostNL locaties</AutoLink>.</p>
</Message><MPImg src="/documentation/magento/magento-maandagbezorging.jpg" alt="Magento maandagbezorging" />
