<h3 id="aantal-dagen-vooruit" tabindex="-1"><a class="header-anchor" href="#aantal-dagen-vooruit" aria-hidden="true">#</a> Aantal dagen vooruit</h3>
<p>Met de functie 'aantal dagen vooruit' kun je aangeven hoeveel dagen vooruit jouw
klant een levermoment kan kiezen. Wanneer je kiest voor 7 dagen is laatste
keuzemogelijkheid precies 7 dagen later. De dagen waarop er geen leveringen
