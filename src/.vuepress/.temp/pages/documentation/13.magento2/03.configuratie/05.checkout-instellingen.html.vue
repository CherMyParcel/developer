<h2 id="belgie-bezorgopties" tabindex="-1"><a class="header-anchor" href="#belgie-bezorgopties" aria-hidden="true">#</a> België bezorgopties</h2>
<p>Naast het feit dat het mogelijk is om jouw pakketten bij een Nederlandse
PostNL-locatie te laten bezorgen, is het ook mogelijk om voor een Belgische
PostNL-locatie te kiezen.</p>
<p><strong>België thuislevering titel</strong>
De titel die je wilt gebruiken voor het thuis laten bezorgen is aan te passen
ten opzichte van een Nederlandse zending.</p>
<p><strong>België standaard bezorgtitel</strong>
De titel die je wilt gebruiken voor een standaard levering naar België is aan te
passen ten opzichte van een Nederlandse zending. Wanneer er geen titel is
ingevuld, worden de bezorgtijden automatisch zichtbaar.</p>
