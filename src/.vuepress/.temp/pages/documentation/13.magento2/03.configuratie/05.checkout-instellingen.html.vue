<h3 id="alleen-huisadres" tabindex="-1"><a class="header-anchor" href="#alleen-huisadres" aria-hidden="true">#</a> Alleen huisadres</h3>
<p>Je kunt hiermee aangeven of jouw klanten hier zelf voor kunnen kiezen in de
checkout. Indien jij dit wilt weergeven in de checkout kies dan voor <code>Ja</code>. Ook
is het mogelijk om aan deze optie een eigen naam mee te geven, in het voorbeeld
