<h3 id="handtekening-voor-ontvangst" tabindex="-1"><a class="header-anchor" href="#handtekening-voor-ontvangst" aria-hidden="true">#</a> Handtekening voor ontvangst</h3>
<p>Je kunt hiermee aangeven of jouw klanten hier zelf voor kunnen kiezen in de
checkout. Indien jij dit wilt weergeven in de checkout kies dan voor <code>Ja</code> Ook is
het mogelijk om aan deze optie een eigen naam mee te geven, in het voorbeeld
heet deze optie 'Handtekening voor ontvangst'. Als laatste optie kun je er ook
een meerprijs aan koppelen, in het voorbeeld kost deze optie € 0.35 extra.</p>
<p>Er is ook een optie gemaakt voor een gecombineerde meerprijs voor de keuzes
